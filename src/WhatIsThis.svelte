<div class="section">
  <h3>What is a Pythagorean Triple?</h3>
  <p>
    A pythagorean triple is a whole number solution for the Pythagorean <br />
    Theorem(more info here). This states that on a right triangle with sides a,
    <br />
    b, and c, with c being the hypotenuse, a^2 + b^2 = c^2. For example, 3, 4, and
    <br />
    5 are a pythagorean triple, since 3^2(or 9) + 4^2(or 16) = 5^2(or 25).
  </p>
</div>

